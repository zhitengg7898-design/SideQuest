function getFirstName(fullName) {
  return fullName.trim().split(/\s+/)[0]?.toLowerCase() ?? "";
}

function guessGender(fullName) {
  const firstName = getFirstName(fullName);

  if (LIKELY_FEMALE_NAMES.has(firstName)) {
    return "female";
  }

  if (LIKELY_MALE_NAMES.has(firstName)) {
    return "male";
  }

  if (/(a|ah|elle|ette|ine|ina|lla|lyn|ie)$/i.test(firstName)) {
    return "female";
  }

  return "male";
}

export function buildProfileAvatarUrl(name) {
  const gender = guessGender(name);
  const params = new URLSearchParams({
    seed: name.trim(),
    gender,
    backgroundColor: "22232f",
  });

  return `https://api.dicebear.com/9.x/personas/svg?${params.toString()}`;
}
